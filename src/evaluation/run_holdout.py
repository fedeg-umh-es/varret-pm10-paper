"""
Run holdout predictions
=======================
Placeholder: Predictions are already generated by train_*.py scripts.
This script is here for potential future use in post-processing.
"""

import pandas as pd
from pathlib import Path
import argparse
import yaml


def main():
    """Verify holdout predictions exist."""
    parser = argparse.ArgumentParser(description="Run holdout (validation script)")
    parser.add_argument("--config", type=str, default="config/config.yaml")
    args = parser.parse_args()
    
    with open(args.config) as f:
        cfg = yaml.safe_load(f)
    
    pred_dir = Path(cfg['paths']['predictions_dir'])
    
    # Check predictions
    models = ['persistence', 'lgbm', 'lstm']
    print("Checking holdout predictions:")
    for model in models:
        path = pred_dir / f"{model}_holdout.parquet"
        if path.exists():
            df = pd.read_parquet(path)
            print(f"✓ {model}: {len(df)} records")
        else:
            print(f"✗ {model}: MISSING")


if __name__ == "__main__":
    main()
